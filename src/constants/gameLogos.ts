export const isBrokenLogoUrl = (url?: string): boolean => {
  if (!url) return true;
  const lower = url.toLowerCase();
  
  // Specific broken screenshots/placeholders on image2url
  if (lower.includes('1780940921769') || lower.includes('1780940834051')) {
    return true;
  }

  return (
    lower.includes('jilislot.com') ||
    lower.includes('evolution.com') ||
    lower.includes('spribe.co')
  );
};

export const getCleanGameLogo = (gameId: string, globalLogos?: Record<string, string>, fallbackImage?: string): string => {
  const dbUrl = globalLogos?.[gameId];
  if (dbUrl && !isBrokenLogoUrl(dbUrl)) {
    return dbUrl;
  }
  return GAME_LOGO_URLS[gameId] || fallbackImage || `https://picsum.photos/seed/${gameId}/400/600`;
};

